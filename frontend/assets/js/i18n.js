  var DICT = { en: EN, de: DE };
  var state = { lang: resolve(), listeners: [] };

  function t(key, subs) {
    var tbl = DICT[state.lang] || EN;
    var s = (tbl && tbl[key] != null) ? tbl[key] : (EN[key] != null ? EN[key] : key);
    if (subs) s = s.replace(/\{(\w+)\}/g, function (m, k) { return subs[k] != null ? subs[k] : m; });
    return s;
  }
  function applyStatic(root) {
    root = root || document;
    var set = function (sel, attr) {
      root.querySelectorAll("[" + sel + "]").forEach(function (el) {
        var val = t(el.getAttribute(sel));
        if (attr === "text") el.textContent = val; else el.setAttribute(attr, val);
      });
    };
    set("data-i18n", "text");
    set("data-i18n-aria", "aria-label");
    set("data-i18n-ph", "placeholder");
    set("data-i18n-title", "title");
    try { document.documentElement.setAttribute("lang", state.lang === "de" ? "de-DE" : "en"); } catch (e) {}
  }
  // app.js owns persistence of "jarvis.lang" (it also stores fr/es/it); here we only track en/de for the UI.
  function setLang(code) {
    state.lang = (code === "de" ? "de" : "en");
    applyStatic(document);
    state.listeners.forEach(function (fn) { try { fn(state.lang); } catch (e) {} });
  }
  function onChange(fn) { if (typeof fn === "function") state.listeners.push(fn); }

  window.JV_I18N = {
    t: t, applyStatic: applyStatic, setLang: setLang, onChange: onChange, dict: DICT,
    get lang() { return state.lang; }
  };

